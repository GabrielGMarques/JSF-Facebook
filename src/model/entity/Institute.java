package model.entity;

public class Institute {
	private String name;
	private String finalYearOfCourse;

	public String getName() {
		return name;
	}

	public void setName(String instituteName) {
		this.name = instituteName;
	}

	public String getFinalYearOfCourse() {
		return finalYearOfCourse;
	}

	public void setFinalYearOfCourse(String finalYearOfCourse) {
		this.finalYearOfCourse = finalYearOfCourse;
	}

}
